package Operators;

public class Prog1 {

	public static void main(String[] args) {
		
			// Arithmetic operator
			int a = 20;
			int b =50;
			int c = a+b;
			System.out.println(c);
			
			
			int d = a-b;
			System.out.println(d);
			
			int e = a*b;
			System.out.println(e);
			
			int f = a/b;
			System.out.println(f);
			
			int g = a%b;
			System.out.println(g);
			
			
			
			
			
			// Assignment operator
			
			int i =4;
			int j=6;
			
			i+=4;
			System.out.println(i);
			
			j-=4;
			System.out.println(i);
			
			i*=4;
			System.out.println(i);
			
			j/=4;
			System.out.println(i);
			
			
			// Ternary operator
			
			int t=8, h=5,Max ;  
			Max = (t<h) ? t:h ;
			System.out.println(Max);
			
			Max = (t>h) ? t:h ;
			System.out.println(Max);
			
			Max = (t==h) ? t:h ;
			System.out.println(Max);
			
			Max = (t!=h) ? t:h ;
			System.out.println(Max);
			Max = (t>=h) ? t:h ;
			System.out.println(Max);
			
			Max = (t<=h) ? t:h ;
			System.out.println(Max);
			
			
			
			
			

	}

}
